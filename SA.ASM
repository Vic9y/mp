ASSUME CS:CODE,DS:DATA

DATA SEGMENT
STRING DB 'SAHIL CHAVAN$'
SLEN EQU ($-STRING)
CHAR DB 'A'
MSG1 DB 'CHARACTER FOUND$'
MSG2 DB 'CHARACTER NOT FOUND$'
DATA ENDS

CODE SEGMENT
START:MOV AX,DATA
MOV DS,AX
MOV ES,AX
LEA SI,STRING
MOV CX,SLEN
MOV AL,CHAR
CLD
REPNE SCASB
JNZ EXIT

LEA DX,MSG1
MOV AH,09H
INT 21H
JMP GOTOEND
EXIT:LEA DX,MSG2
MOV AH,09H
INT 21H
GOTOEND:MOV AH,4CH
INT 21H
CODE ENDS
END START
